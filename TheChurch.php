										<h3>About the church</h3>
								  </header>
<p>RCCG Victory International Center is a dynamic non-denominational, international church where praise is exuberant, prayer is steadfast, and the preaching is practical.</p>

<p>The first fellowship meeting was held in the city of Atlanta, May, 1997 and our Senior Pastor, Akorede Akindele has presided over the congregation since inception. In 2003, the church moved to its current location in Mableton, Cobb County. RCCGVIC is conveniently located a mile from the Mableton Art Center and a mile from South Cobb High School. More than 200 people consider it their home church with over thirty faithful volunteers, committed to serving Christ in 10 outreach ministries. We believe the church is strategically positioned to impact its multi-racial community with a glimpse of what Heaven will look like.</p>


<p>This church has become undoubtedly a Pillar and Spiritual icon through the extraordinary Outreach work done in the community. Our flagship BACK-2-SCHOOL program provides up to 400 Backpacks, Polo T-shirts, notebooks, pencils, pens, and haircuts among other gifts to needy children in our community when it's time for kids to go back to school. We give Turkeys and canned foods to the poor, needy and the unemployed during our annual Thanksgiving Giveaways so that the love of God is shown and represented in the community through RCCG Victory International Center. We collaborate with Toys for Torts and United Ways to distribute Toys, Gifts, Bibles, Clothes, Shoes, Games, Books, Notebooks, and other blessings during our annual Christmas concert program and the good tidings and the joy of Christmas is noticeably visible on the faces of Children and Parents as they receive gifts and celebrate the birth of our Lord and Savior, Jesus Christ. These are just few representatives of the programs that we as a Church devote our time and resources to in the community.</p>

