                                        <h3>
                                        What We Believe                                      
                                        </h3>
									</header>
<b>God the Father</b>
<p>Creator of Heaven and Earth, who made man in His own image and likeness (Gen 1:1,26-27, John 4:23,34, John 10:14-15, 1 John 5:6-7).</p>

<b>Jesus Christ</b>
<p>The only begotten Son of God the Father. We believe in the deity of our Lord Jesus Christ, in His virgin birth, in His sinless life, in His miracles, in His vicarious and atoning death through His shed blood, in His bodily resurrection, in His ascension to the right hand of the Father, and in His personal return in power and glory (John 3:16, I Corinthians 15:3-4, I Peter 2:18-24, Heb 1:1-5).</p>

<b>The Holy Spirit</b>
<p>The comforter and teacher of the Holy Spirit dwell within us, and unite us unto Jesus Christ. (John 14:15-18)</p>

<b>The Baptism of the Holy Spirit</b>
<p>Is available to all who believe in Jesus Christ, and is evidenced by the gift of speaking in tongues. (Act 1:8, Act 2:1-4)</p>


<b>Water Baptism</b>
<p>That baptism can only be by immersion in water as Jesus Christ was baptized. Baptism in water is a declaration to the world that a believer has died with Christ and that they have been raised with Him to walk in newness of life (Mt. 28:19; Acts 10:47-48; Rom. 6:4)</p>


<b>Salvation</b>
<p>It is a gift from God through grace and faith in Christ Jesus. The salvation of lost and sinful man is absolutely essential. All who believe in the name of Jesus Christ may be saved by turning from sin to repentance and are born again by the Holy Spirit through trusting in His death and resurrection (John 1:12, Romans 3:21-30, Rom 10: 9-10, Galatians 4:4-7, and Eph 2:8-9)</p>

<b>The Blood of Jesus Christ</b>
<p>The blood of Christ was shed for us at Calvary, cleanses us of all sin and grants us access into the presence of God (Isaiah 53:5, I Peter 2:24)</p>

<b>The Redemptions Work</b>
<p>The redemptive works of Jesus Christ is not limited to cleansing of sin but on the cross we are afforded with deliverance from sickness, disease, and poverty. This is a redemptive privilege of all believers in Christ. (Isaiah 53:4-5, Matthew 8:16-17, Mark 16:18, John 5:6-14, and I Peter 2:24)</p>

<b>The second coming of Jesus Christ</b>
<p>That Jesus Christ will come again to earth in glory and in power. That those dead in Christ will rise first and those alive in Christ will be in the presence of God for eternity. After this second coming of Jesus Christ to rapture His church He will also to judge the world. (John 14:1-3, 1 Corinthian. 15: 50 - 52, I Thessalonians 4:13-18, and Revelation 22:20)</p>

<b>Sanctification</b>
<p>Separation from the world and embracing God is a life of true holiness. The sanctifying power of the Holy Spirit by whose indwelling the Christian is enabled to live a holy life. (Zechariah 4:6, Romans 8:1-14, 2 Cor. 5:17, Titus 3:4-5, 1 John 2: 15-17)</p>

<b>The Holy Bible</b>
<p>Is the infallible and authoritative Word of God, given directly to all men and women as a thorough equipping tool for doctrine, reproof, correction, instruction and salvation. (2 Tim 3:16-17, Heb 4:12)</p>
